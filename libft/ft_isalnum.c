#include "libft.h"

int		ft_isalnum(int c)
{
	return ((((unsigned char)c >= 060 && (unsigned char)c <= 071) ||
			((unsigned char)c >= 0101 && (unsigned char)c <= 0172)) ? 1 : 0);
}
